    private void ParcourirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ParcourirActionPerformed
        // l'ouverture du gestionnaire de fichier lors du clic sur ce  bouton
        JFileChooser jf= new JFileChooser();
        if (jf.showOpenDialog(this)== JFileChooser.APPROVE_OPTION)
    {
        String path = jf.getSelectedFile().getPath();
        Image img=Toolkit.getDefaultToolkit().getImage(path);
        img=img.getScaledInstance(Photo.getWidth()-10, Photo.getHeight()-10, Image.SCALE_DEFAULT);
        photo = new ImageIcon(img);
        Photo.setIcon(photo);
    }
    }//GEN-LAST:event_ParcourirActionPerformed

    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        // verifie si la famille est bien selectionné et pseudo rempli avant de valider
        String p;
        int i=ListeFamilles.getSelectedIndex();
        if(!Edition.getText().equals("")&&i!=-1){
            String f=ListeFamilles.getSelectedValue().toString();
            p=Edition.getText();
            this.nouveau_joueur =new Joueur(p,f);
            if(photo!=null){
                nouveau_joueur.setPhoto(photo);
            }
            this.fermeture=true;
            this.setVisible(false);
        }
        else{
            Edition.setText("Le pseudo est vide, veuillez le remplir"); //message d'erreur si le pseudo est vide
            this.fermeture=false;
            this.setVisible(true);
        }
    }//GEN-LAST:event_ValiderActionPerformed

