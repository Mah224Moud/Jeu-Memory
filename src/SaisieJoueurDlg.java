    private void initListeFamilles()
    {
        DefaultListModel model= (DefaultListModel)ListeFamilles.getModel();
        ListeFamilles.setModel(model);
        for (int i=0; i<this.lp.getFamilles().size(); i++)
        {
            model.addElement(this.lp.getFamilles().get(i));
        }
    }
   
    
